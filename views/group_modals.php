                  The use of choirs in the Church is a tradition that dates back to the very beginning of Christianity.

                    Here at Our Savior Lutheran, we are proud to keep that wonderful tradition alive in honoring and praising God.

                    The "Celebration Choir" is a choral group that plays an important role in the Sunday morning worship experience, and it actively participates in both the 8 am and 10:45 am worship services. The repertoire of the choir includes a wide variety of sacred musical styles, from Gregorian chant, through traditional/classical repertoire, to contemporary and folk selections.&nbsp; The choir performs three Sundays a month, as well as at special church holidays and occasions.

                    <strong>Choir rehearsals</strong> are held on <strong>Thursdays, 7 pm - 8:30 pm</strong>

                    <strong>
                    </strong>
                    The "Carillon" Hand Bell Choir plays worship one Sunday a month, and at special church holidays and occasions. Many of our hand bell players are members of our choral group as well. The repertoire of the hand bell choir includes both arrangements of sacred classical and contemporary music.

                    <strong>Bell rehearsals </strong>are held on<strong> Thursdays, 6pm - 7pm.</strong>

                    On many occasions throughout the year, we invite musicians from our congregation and from the community to enrich our worship with their talents.

                    During the summer months, Sunday morning worship is musically supported by our vocal and instrumental soloists.

                    For more information or if you are interested in joining, please come to one of our rehearsals or contact our Music Director, <span class="il">Marija</span>.
